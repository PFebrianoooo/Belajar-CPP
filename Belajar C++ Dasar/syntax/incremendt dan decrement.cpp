#include <iostream>
using namespace std;

int main (){
	
	// increment dan decrement 
	// pre increment dan post increment
	
	int a = 5;
	int b = 5;
	
	// standar 
	cout << a << endl;
	// post increment 
	a++;
	cout << a << endl;
	// pre increment
	++b;
	cout << b << endl;
	
	
	// standar 
	cout << b << endl;
	// post decrement
	b--;
	cout << b << endl;
	// pre decrement 
	--b;
	cout << b << endl; 
	
	
	
	return 0;
}
